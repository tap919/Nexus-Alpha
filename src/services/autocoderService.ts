// ─── Database Schema Generator ──────────────────────────────────────────────────

const databaseSchemaGenerator: TemplateGenerator = {
  supportsLanguage: (lang) => ["sql", "typescript", "ts"].includes(lang),

  generate(input): string {
    const { name, options } = input;
    const tableName = toSnakeCase(name);
    const dbType = options?.database || "postgresql";

    let code = "";

    if (options?.addDocs) {
      code += `-- ${toPascalCase(name)} table\n`;
      code += `-- Generated by AutoCoder\n\n`;
    }

    if (dbType === "postgresql" || dbType === "supabase") {
      code += `CREATE TABLE ${tableName} (\n`;
      code += `  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),\n`;
      code += `  name VARCHAR(255) NOT NULL,\n`;
      code += `  description TEXT,\n`;
      code += `  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),\n`;
      code += `  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()\n`;
      code += `);\n\n`;
      code += `-- Enable Row Level Security\n`;
      code += `ALTER TABLE ${tableName} ENABLE ROW LEVEL SECURITY;\n\n`;
      code += `-- Create indexes\n`;
      code += `CREATE INDEX idx_${tableName}_created_at ON ${tableName}(created_at);\n`;
    } else if (dbType === "mysql") {
      code += `CREATE TABLE ${tableName} (\n`;
      code += `  id VARCHAR(36) PRIMARY KEY,\n`;
      code += `  name VARCHAR(255) NOT NULL,\n`;
      code += `  description TEXT,\n`;
      code += `  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,\n`;
      code += `  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP\n`;
      code += `);\n\n`;
      code += `CREATE INDEX idx_${tableName}_created_at ON ${tableName}(created_at);\n`;
    } else {
      code += `// Prisma schema for ${toPascalCase(name)}\n`;
      code += `model ${toPascalCase(name)} {\n`;
      code += `  id        String   @id @default(uuid())\n`;
      code += `  name      String\n`;
      code += `  description String?\n`;
      code += `  createdAt DateTime @default(now())\n`;
      code += `  updatedAt DateTime @updatedAt\n`;
      code += `}\n`;
    }

    return code;
  },
};

