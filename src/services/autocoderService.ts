/**
 * AutoCoder - Deterministic Code Generation Service
 *
 * Generates boilerplate code using template-based algorithms instead of LLM calls.
 * This reduces token usage by 100% for common patterns that can be deterministically generated.
 *
 * Supported patterns:
 * - CRUD operations
 * - REST API endpoints
 * - React components
 * - TypeScript types/interfaces
 * - Database schemas
 * - Test files
 * - Configuration files
 */
import { logger } from "../lib/logger";

export type CodePattern =
  | "crud-api"
  | "react-component"
  | "typescript-interface"
  | "database-schema"
  | "test-file"
  | "config-file"
  | "hook"
  | "service"
  | "model"
  | "middleware";

export interface AutocoderInput {
  pattern: CodePattern;
  name: string;
  options?: {
    language?: string;
    framework?: string;
    database?: string;
    addTypes?: boolean;
    addTests?: boolean;
    addDocs?: boolean;
  };
}

export interface AutocoderResult {
  success: boolean;
  files: Array<{ path: string; content: string }>;
  tokenSavings: number;
  pattern: CodePattern;
  message?: string;
}

interface TemplateGenerator {
  generate(input: AutocoderInput): string;
  supportsLanguage(lang: string): boolean;
}

// ─── TypeScript Interface Generator ────────────────────────────────────────────

const typeScriptInterfaceGenerator: TemplateGenerator = {
  supportsLanguage: (lang) => ["typescript", "ts", "tsx"].includes(lang),

  generate(input): string {
    const { name, options } = input;
    const pascalName = toPascalCase(name);
    const typeName = options?.addTypes ? `${pascalName}Type` : pascalName;

    let fields = "";
    switch (options?.framework) {
      case "react":
        fields = `  id: string;
  createdAt: Date;
  updatedAt: Date;`;
        break;
      case "api":
        fields = `  id: string;
  name: string;
  description?: string;
  createdAt: string;
  updatedAt: string;`;
        break;
      default:
        fields = `  id: string;
  name: string;
  value: unknown;`;
    }

    let interfaceCode = `export interface ${typeName} {\n${fields}\n}\n`;

    if (options?.addTypes) {
      interfaceCode += `\nexport type ${pascalName}Input = Partial<Omit<${typeName}, "id" | "createdAt" | "updatedAt">>>;\n`;
      interfaceCode += `export type ${pascalName}Output = ${typeName};\n`;
    }

    if (options?.addDocs) {
      interfaceCode = `/**\n * ${pascalName} interface\n * Generated by AutoCoder\n */\n\n` + interfaceCode;
    }

    return interfaceCode;
  },
};

