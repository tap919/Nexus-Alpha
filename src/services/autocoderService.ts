// ─── Generator Registry ────────────────────────────────────────────────────────

const GENERATORS: Record<CodePattern, TemplateGenerator> = {
  "crud-api": crudApiGenerator,
  "react-component": reactComponentGenerator,
  "typescript-interface": typeScriptInterfaceGenerator,
  "database-schema": databaseSchemaGenerator,
  "test-file": testFileGenerator,
  "config-file": configFileGenerator,
  "hook": reactComponentGenerator,
  "service": serviceGenerator,
  "model": modelGenerator,
  "middleware": middlewareGenerator,
};

// ─── Helper Functions ────────────────────────────────────────────────────────────

function toPascalCase(str: string): string {
  return str
    .replace(/[-_\s]+(.)?/g, (_, c) => (c ? c.toUpperCase() : ""))
    .replace(/^(.)/, (c) => c.toUpperCase());
}

function toCamelCase(str: string): string {
  const pascal = toPascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

function toSnakeCase(str: string): string {
  return str
    .replace(/([A-Z])/g, "_$1")
    .toLowerCase()
    .replace(/^_/, "")
    .replace(/[-_\s]+/g, "_");
}

// ─── Main API ─────────────────────────────────────────────────────────────────

export function generateCode(input: AutocoderInput): AutocoderResult {
  const { pattern, name, options } = input;
  const generator = GENERATORS[pattern];

  if (!generator) {
    return {
      success: false,
      files: [],
      tokenSavings: 0,
      pattern,
      message: `Unknown pattern: ${pattern}`,
    };
  }

  try {
    const content = generator.generate(input);
    const tokenEstimate = Math.ceil(content.length / 4);

    logger.info("AutoCoder", `Generated ${pattern} for "${name}" - saved ~${tokenEstimate} tokens`);

    return {
      success: true,
      files: [
        {
          path: getDefaultFilePath(pattern, name, options),
          content,
        },
      ],
      tokenSavings: tokenEstimate,
      pattern,
      message: `Generated ${pattern} code`,
    };
  } catch (error) {
    logger.error("AutoCoder", `Failed to generate ${pattern}: ${error}`);
    return {
      success: false,
      files: [],
      tokenSavings: 0,
      pattern,
      message: `Generation failed: ${error}`,
    };
  }
}

function getDefaultFilePath(pattern: CodePattern, name: string, options?: AutocoderInput["options"]): string {
  const baseName = toPascalCase(name);
  switch (pattern) {
    case "crud-api":
      return options?.framework === "express" ? `src/routes/${toCamelCase(name)}.ts` : `src/services/${baseName}Service.ts`;
    case "react-component":
      return `src/components/${baseName}/${baseName}.tsx`;
    case "typescript-interface":
      return `src/types/${baseName}.ts`;
    case "database-schema":
      return `database/schema.sql`;
    case "test-file":
      return `tests/${baseName}.test.ts`;
    case "config-file":
      return options?.language === "yaml" ? "config.yaml" : "config.json";
    case "hook":
      return `src/hooks/use${baseName}.ts`;
    case "service":
      return `src/services/${baseName}Service.ts`;
    case "model":
      return `src/models/${baseName}.ts`;
    case "middleware":
      return `src/middleware/${toCamelCase(name)}.ts`;
    default:
      return `src/${name}.ts`;
  }
}

export function canGeneratePattern(pattern: CodePattern): boolean {
  return pattern in GENERATORS;
}

export function getSupportedPatterns(): CodePattern[] {
  return Object.keys(GENERATORS) as CodePattern[];
}

export function estimateTokenSavings(pattern: CodePattern): number {
  const estimates: Record<CodePattern, number> = {
    "crud-api": 150,
    "react-component": 80,
    "typescript-interface": 40,
    "database-schema": 60,
    "test-file": 100,
    "config-file": 30,
    "hook": 120,
    "service": 90,
    "model": 70,
    "middleware": 85,
  };
  return estimates[pattern] || 50;
}
